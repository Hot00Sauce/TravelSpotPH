    <h2>Bohol</h2>
    <p>Bohol is a province of the Philippines, in the country’s Central Visayas region. It comprises Bohol Island and numerous smaller surrounding islands. Bohol is known for coral reefs and unusual geological formations, notably the Chocolate Hills. On the main island, near the town of Carmen, these 1,200 or so symmetrical mounds turn cocoa-brown in the dry season, contrasting with the surrounding jungle's greenery.<br>It is the largest province in the country in terms of total area of 14,649.73 km². <br>The capital city is Puerto Princesa.</p>
